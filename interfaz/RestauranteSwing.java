    public RestauranteSwing() throws InterruptedException {
        initComponents();
        setVisible(true);
        
        //Inicializamos un array de string donde se guardan los mensajes
        //que se pasaran mediante sockets
        mensajes = new ArrayList<>();
        mensajes.add(mostradortxt);
        mensajes.add(mesatxt);
        
        //Creamos un objeto de tipo servidor que tiene como parametro los
        //mensajes que queremos pasar, e iniciamos el hilo
        
        Servidor s = new Servidor(mensajes);
        s.start();
        
        //Creamos e inicializamos el mostrador y la mesa
        //Creamos el cliente y un arraylist de clientes donde se iran
        //introduciendo despues de crearse
        Mostrador most = new Mostrador(10, mostradortxt);
        Mesa mesa = new Mesa(20, mesatxt);
        ClienteT c;
        ArrayList<ClienteT> clientes = new ArrayList<ClienteT>();

        //Con este bucle creamos los 200 clientes, los a√±adimos
        //al arraylist de clientes e iniciamos los hilos
        
        for (int i=0; i<200; i++){
             c = new ClienteT("Cliente"+i, most);
             clientes.add(c);
             c.start();
        }
        
        //Creamos e inicializamos los 2 empleados y los 3 cocineros
        
        emp1 = new EmpleadoT("Empleado1", most, mesa, empleado1txt);
        emp2 = new EmpleadoT("Empleado2", most, mesa, empleado2txt);
        coc1 = new CocineroT("Cocinero1", mesa, cocinero1txt);
        coc2 = new CocineroT("Cocinero2", mesa, cocinero2txt);
        coc3 = new CocineroT("Cocinero3", mesa, cocinero3txt);
        
        //Iniciamos sus respectivos hilos
        emp1.start();
        emp2.start();       
        coc1.start();       
        coc2.start();      
        coc3.start();
        
        //Recorremos el array de clientes para indicarles
        //que esperen a que se termine la ejecucion
        
        for(int j=0; j<clientes.size(); j++){
            
            clientes.get(j).join();
        }
    }
    
    

